import { build } from "vite";
import { readFileSync, writeFileSync, mkdirSync, copyFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";
const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const ROUTE = "/military-relocation/buckley-space-force-base/";
const ARTIFACT = "buckley-space-force-base.html";
console.log(`[prerender-buckley-space-force-base] Starting SSR build for ${ROUTE}...`);
await build({ root: ROOT, build: { ssr: resolve(ROOT, "client/src/entry-server-buckley-space-force-base.tsx"), outDir: resolve(ROOT, "dist/server"), emptyOutDir: false, rollupOptions: { output: { entryFileNames: "entry-server-buckley-space-force-base.js" } } }, logLevel: "warn" });
console.log(`[prerender-buckley-space-force-base] SSR build complete. Rendering HTML...`);
const { renderBuckleySpaceForceBase } = await import(resolve(ROOT, "dist/server/entry-server-buckley-space-force-base.js"));
const appHtml = renderBuckleySpaceForceBase();
const template = readFileSync(resolve(ROOT, "dist/public/index.html"), "utf-8");
const html = template.replace('<div id="root"></div>', `<div id="root">${appHtml}</div>`);
console.log(`[prerender-buckley-space-force-base] Rendered HTML length: ${appHtml.length} chars`);
const prerenderedDir = resolve(ROOT, "server/prerendered");
mkdirSync(prerenderedDir, { recursive: true });
writeFileSync(resolve(prerenderedDir, ARTIFACT), html);
console.log(`[prerender-buckley-space-force-base] Written to ${resolve(prerenderedDir, ARTIFACT)} (${Buffer.byteLength(html)} bytes)`);
const distPrerenderedDir = resolve(ROOT, "dist/prerendered");
mkdirSync(distPrerenderedDir, { recursive: true });
copyFileSync(resolve(prerenderedDir, ARTIFACT), resolve(distPrerenderedDir, ARTIFACT));
console.log(`[prerender-buckley-space-force-base] Written to ${resolve(distPrerenderedDir, ARTIFACT)} (fallback)`);
console.log(`[prerender-buckley-space-force-base] Done.`);
